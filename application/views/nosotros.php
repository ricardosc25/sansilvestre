<div class="page-header">
  <h1><div class="block-title"><span class="deco"></span><span class="text">Quiénes somos</span></div></h1>
  <p>La Fundación Carrera Internacional San Silvestre Rafael E. Guzmán M. es una persona jurídica, de derecho civil, de interés social, sin ánimo de lucro, que tiene por objeto social primordial, organizar y promover la Carrera Internacional San Silvestre que se realiza todos los 31 de Diciembre de cada año en el Distrito Especial Industrial y Portuario de Barranquilla, en homenaje al deportista directivo Rafael Enrique Guzmán Mendoza (1952-2012). La fundación en unión con el Club Deportivo de Atletismo Barrio Chiquinquirá, viene fomentando la práctica deportiva del atletismo, la recreación y el aprovechamiento del tiempo libre.</p>
</div>

<div class="page-header">
  <h1> <div class="block-title"><span class="deco"></span><span class="text">Misión</span></div></h1>
  <p>Fundación Carrera Internacional San Silvestre “Rafael E. Guzmán M.” Y el  Club De Atletismo Barrio Chiquinquirá tiene como propósito fomentar, reglamentar y patrocinar la práctica de la disciplina deportiva atletismo, la recreación y el aprovechamiento del tiempo libre en el Distrito e impulsar programas de interés público y social.</p>
</div>

<div class="page-header">
  <h1> <div class="block-title"><span class="deco"></span><span class="text">Visión</span></div></h1>
  <p>Ser un instrumento dinamizador que Apoya la conservación del legado cultural y deportivo dejado por el creador de la Carrera Internacional San Silvestre Rafael Guzmán” de la ciudad de Barranquilla y que a través del tiempo permanezca y crezca cada año.</p><br
</div>

<div class="page-header">
  <h1> <div class="block-title"><span class="deco"></span><span class="text">Objetivos</span></div></h1>
   <ul>
  <li>Generar la práctica de la disciplina deportiva atletismo, la recreación y el aprovechamiento del tiempo libre en el Distrito de Barranquilla.</li>
    <li>Impulsar programas de interés.</li>
    <li>Fomentar eventos deportivos que estimulen la participación de los deportistas elites nacionales e internacionales.</li>
    <li>Impulsar este deporte en la niñez para desarrollo de una mejor calidad de vida.</li>
    <li>Incentivar la cultura al atletismo en las categorías recreativas.</li>
    <li>Potenciar la amistad y la convivencia en la comunidad Atlanticense y el intercambio cultural con todas las regiones colombianas y visitantes de otras nacionalidades.</li>
    <li>Posicionar a Barranquilla como ciudad con cultura deportiva en Colombia y en el mundo.</li>
    <li>Mostrar la estructura social y turística de Barranquilla.</li>
    <li>Brindar eventos deportivos que ayuden a la cultura saludable de nuestra sociedad en general.</li>
  </ul>
